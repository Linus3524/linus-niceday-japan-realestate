const matcher = new RegExp(Object.keys(readings).sort((a, b) => b.length - a.length).join("|"), "g");

const toHiragana = (reading: string) =>
  [...reading].map((character) => {
    const code = character.charCodeAt(0);
    return code >= 0x30a1 && code <= 0x30f6
      ? String.fromCharCode(code - 0x60)
      : character;
  }).join("");

export function JapaneseRuby({ text }: { text: string }) {
  const nodes: ReactNode[] = [];
  let lastIndex = 0;
  let match: RegExpExecArray | null;
  let index = 0;

  while ((match = matcher.exec(text)) !== null) {
    if (match.index > lastIndex) nodes.push(text.slice(lastIndex, match.index));
    nodes.push(
      <ruby key={`${match[0]}-${index++}`} className="whitespace-nowrap [ruby-position:over]">
        {match[0]}<rt className="relative -top-[0.08em] select-none font-sans text-[0.5em] font-semibold tracking-normal text-[#00a174]">{toHiragana(readings[match[0]])}</rt>
      </ruby>
    );
    lastIndex = match.index + match[0].length;
  }

  if (lastIndex < text.length) nodes.push(text.slice(lastIndex));
  return <>{nodes}</>;
}
